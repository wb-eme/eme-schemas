<?php
declare(strict_types=1);

// @link http://schemas.wbeme.com/json-schema/eme/iam/node/user/1-0-0.json#
namespace Eme\Schemas\Iam\Node;

use Eme\Schemas\Sys\TenantId;
use Gdbots\Pbj\AbstractMessage;
use Gdbots\Pbj\Enum\Format;
use Gdbots\Pbj\FieldBuilder as Fb;
use Gdbots\Pbj\Schema;
use Gdbots\Pbj\Type as T;
use Gdbots\Pbj\WellKnown\UuidIdentifier;
use Gdbots\Schemas\Iam\Mixin\User\UserV1Trait as GdbotsIamUserV1Trait;
use Gdbots\Schemas\Ncr\Enum\NodeStatus;
use Gdbots\Schemas\Ncr\Mixin\Node\NodeV1Trait as GdbotsNcrNodeV1Trait;

final class UserV1 extends AbstractMessage
{
    const SCHEMA_ID = 'pbj:eme:iam:node:user:1-0-0';
    const SCHEMA_CURIE = 'eme:iam:node:user';
    const SCHEMA_CURIE_MAJOR = 'eme:iam:node:user:v1';

    const MIXINS = [
      'eme:sys:mixin:tenant-id:v1',
      'eme:sys:mixin:tenant-id',
      'gdbots:ncr:mixin:node:v1',
      'gdbots:ncr:mixin:node',
      'gdbots:iam:mixin:user:v1',
      'gdbots:iam:mixin:user',
      'gdbots:common:mixin:taggable:v1',
      'gdbots:common:mixin:taggable',
    ];

    const TENANT_ID_FIELD = 'tenant_id';
    const _ID_FIELD = '_id';
    const STATUS_FIELD = 'status';
    const ETAG_FIELD = 'etag';
    const CREATED_AT_FIELD = 'created_at';
    const CREATOR_REF_FIELD = 'creator_ref';
    const UPDATED_AT_FIELD = 'updated_at';
    const UPDATER_REF_FIELD = 'updater_ref';
    const LAST_EVENT_REF_FIELD = 'last_event_ref';
    const TITLE_FIELD = 'title';
    const FIRST_NAME_FIELD = 'first_name';
    const LAST_NAME_FIELD = 'last_name';
    const EMAIL_FIELD = 'email';
    const EMAIL_DOMAIN_FIELD = 'email_domain';
    const PHONE_FIELD = 'phone';
    const NETWORKS_FIELD = 'networks';
    const IS_BLOCKED_FIELD = 'is_blocked';
    const IS_STAFF_FIELD = 'is_staff';
    const ROLES_FIELD = 'roles';
    const TAGS_FIELD = 'tags';

    const FIELDS = [
      self::TENANT_ID_FIELD,
      self::_ID_FIELD,
      self::STATUS_FIELD,
      self::ETAG_FIELD,
      self::CREATED_AT_FIELD,
      self::CREATOR_REF_FIELD,
      self::UPDATED_AT_FIELD,
      self::UPDATER_REF_FIELD,
      self::LAST_EVENT_REF_FIELD,
      self::TITLE_FIELD,
      self::FIRST_NAME_FIELD,
      self::LAST_NAME_FIELD,
      self::EMAIL_FIELD,
      self::EMAIL_DOMAIN_FIELD,
      self::PHONE_FIELD,
      self::NETWORKS_FIELD,
      self::IS_BLOCKED_FIELD,
      self::IS_STAFF_FIELD,
      self::ROLES_FIELD,
      self::TAGS_FIELD,
    ];

    use GdbotsNcrNodeV1Trait;

    use GdbotsIamUserV1Trait;

    protected static function defineSchema(): Schema
    {
        return new Schema(self::SCHEMA_ID, __CLASS__,
            [
                /*
                 * The EME tenant that this message is associated with.
                 */
                Fb::create(self::TENANT_ID_FIELD, T\IdentifierType::create())
                    ->required()
                    ->className(TenantId::class)
                    ->build(),
                /*
                 * The "_id" value:
                 * - MUST NOT change for the life of the node.
                 * - SHOULD be globally unique
                 * - SHOULD be generated by the app (ideally in default value closure... e.g. UuidIdentifier::generate())
                 */
                Fb::create(self::_ID_FIELD, T\IdentifierType::create())
                    ->required()
                    ->withDefault(function() { return UuidIdentifier::generate(); })
                    ->className(UuidIdentifier::class)
                    ->overridable(true)
                    ->build(),
                Fb::create(self::STATUS_FIELD, T\StringEnumType::create())
                    ->withDefault("draft")
                    ->className(NodeStatus::class)
                    ->build(),
                Fb::create(self::ETAG_FIELD, T\StringType::create())
                    ->maxLength(100)
                    ->pattern('^[\w\.:-]+$')
                    ->build(),
                Fb::create(self::CREATED_AT_FIELD, T\MicrotimeType::create())
                    ->build(),
                /*
                 * A fully qualified reference to what created this node. This is intentionally a message-ref
                 * and not a user id because it is often a program that creates nodes, not a user.
                 */
                Fb::create(self::CREATOR_REF_FIELD, T\MessageRefType::create())
                    ->build(),
                Fb::create(self::UPDATED_AT_FIELD, T\MicrotimeType::create())
                    ->useTypeDefault(false)
                    ->build(),
                /*
                 * A fully qualified reference to what updated this node. This is intentionally a message-ref
                 * and not a user id because it is often a program that updates nodes, not a user.
                 * E.g. "acme:iam:node:app:cli-scheduler" or "acme:iam:node:user:60c71df0-fda8-11e5-bfb9-30342d363854"
                 */
                Fb::create(self::UPDATER_REF_FIELD, T\MessageRefType::create())
                    ->build(),
                /*
                 * A reference to the last event that changed the state of this node.
                 * E.g. "acme:blog:event:article-published:60c71df0-fda8-11e5-bfb9-30342d363854"
                 */
                Fb::create(self::LAST_EVENT_REF_FIELD, T\MessageRefType::create())
                    ->build(),
                Fb::create(self::TITLE_FIELD, T\StringType::create())
                    ->build(),
                Fb::create(self::FIRST_NAME_FIELD, T\StringType::create())
                    ->build(),
                Fb::create(self::LAST_NAME_FIELD, T\StringType::create())
                    ->build(),
                Fb::create(self::EMAIL_FIELD, T\StringType::create())
                    ->format(Format::EMAIL())
                    ->build(),
                Fb::create(self::EMAIL_DOMAIN_FIELD, T\StringType::create())
                    ->format(Format::HOSTNAME())
                    ->build(),
                /*
                 * A general format for international telephone numbers.
                 * @link https://en.wikipedia.org/wiki/E.164
                 */
                Fb::create(self::PHONE_FIELD, T\StringType::create())
                    ->asAMap()
                    ->pattern('^\+?[1-9]\d{1,14}$')
                    ->build(),
                /*
                 * Networks is a map that contains handles/usernames on a social network.
                 * E.g. facebook:homer, twitter:stackoverflow, youtube:coltrane78.
                 */
                Fb::create(self::NETWORKS_FIELD, T\StringType::create())
                    ->asAMap()
                    ->maxLength(50)
                    ->pattern('^[\w\.-]+$')
                    ->build(),
                Fb::create(self::IS_BLOCKED_FIELD, T\BooleanType::create())
                    ->build(),
                /*
                 * Indicates that the user is a staff member and has access to the cms, dashboard, etc.
                 */
                Fb::create(self::IS_STAFF_FIELD, T\BooleanType::create())
                    ->build(),
                /*
                 * A user's roles determine what permissions they'll have when using the system.
                 * The role itself is a node which has a set of "allow" and "deny" rules.
                 */
                Fb::create(self::ROLES_FIELD, T\NodeRefType::create())
                    ->asASet()
                    ->build(),
                /*
                 * Tags is a map that categorizes data or tracks references in
                 * external systems. The tags names should be consistent and descriptive,
                 * e.g. fb_user_id:123, salesforce_customer_id:456.
                 */
                Fb::create(self::TAGS_FIELD, T\StringType::create())
                    ->asAMap()
                    ->pattern('^[\w\/\.:-]+$')
                    ->build(),
            ],
            self::MIXINS
        );
    }
}
